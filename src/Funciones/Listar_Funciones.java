    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        DefaultTableModel tablemodel = (DefaultTableModel) Table.getModel();
        tablemodel.setRowCount(0);
        ArrayList<String[]> result = new ArrayList<String[]>();

        cadena = "SELECT routine_name FROM information_schema.routines WHERE routine_type='FUNCTION' AND specific_schema='public';";
        result = con.listar_funciones(cadena);
        int i = 0;
        int contador = result.size();

        while (contador > 0) {
            String row[] = new String[result.get(i).length];
            row = result.get(i);
            tablemodel.addRow(new Object[]{row[0]});
            contador--;
            i++;
        }

    }//GEN-LAST:event_jButton1ActionPerformed

